<script>
import { gsap } from 'gsap';
import { ScrollTrigger } from 'gsap/ScrollTrigger';
import { reactive, computed } from '@vue/runtime-core';
import store from '../../store/index';

export default {
    data() {
        return {};
    },
    methods: {
        triggerAnimation(thumbNails, offset) {
            let background = document.querySelector(
                '#pillar-intro .section-4 .img-background'
            );

            let wrapper = document.querySelector(
                '#pillar-intro .section-4 .content-wrapper'
            );

            let titleDescription = document.querySelectorAll(
                '#pillar-intro .section-4 .title-description'
            );

            let impactDetail = document.querySelectorAll(
                '#pillar-intro .section-4 .impact-detail'
            );

            let rightContent = document.querySelectorAll(
                '#pillar-intro .section-4 .right-content'
            );

            let index = thumbNails.indexOf(
                document.querySelector('#pillar-intro .section-4')
            );

            let timeline = gsap.timeline({
                scrollTrigger: {
                    invalidateOnRefresh: store.getters.stInvalidateOnRefresh(),
                    scrub: false,
                    start: store.state.mobileView
                        ? 'top 70%'
                        : offset[index] - window.innerWidth * 0.9,
                    end:
                        !store.state.mobileView &&
                        offset[index] + window.innerWidth * 0.1,
                    trigger: '#pillar-intro .section-4',
                    markers: store.state.markers,
                    id: 'section-4',
                    toggleActions: store.getters.animationDesktopReverse(),
                },
                defaults: { ease: 'power1.out' },
            });

            if (store.state.mobileView) {
                timeline
                    .fromTo(
                        titleDescription,
                        {
                            autoAlpha: 0,
                            y: 5,
                        },
                        {
                            y: 0,
                            autoAlpha: 1,
                            duration: 0.2,
                        }
                    )
                    .fromTo(
                        impactDetail,
                        {
                            autoAlpha: 0,
                            x: -30,
                        },
                        {
                            x: 0,
                            autoAlpha: 1,
                            stagger: 0.05,
                        }
                    )
                    .fromTo(
                        rightContent,
                        {
                            autoAlpha: 0,
                        },
                        {
                            autoAlpha: 1,
                            duration: 0.2,
                        }
                    );
            } else {
                timeline
                    .fromTo(
                        wrapper,
                        {
                            width: 0,
                        },
                        {
                            width: 'auto',
                            duration: 0.1,
                            // delay: store.state.mobileView ? 0 : 0.1,
                        }
                    )
                    .fromTo(
                        titleDescription,
                        {
                            autoAlpha: 0,
                            y: 50,
                        },
                        {
                            y: 0,
                            autoAlpha: 1,
                            duration: 0.5,
                        }
                    )
                    .fromTo(
                        impactDetail,
                        {
                            autoAlpha: 0,
                            x: -30,
                        },
                        {
                            x: 0,
                            autoAlpha: 1,
                            stagger: 0.05,
                        },
                        '-=0.35'
                    )
                    .fromTo(
                        rightContent,
                        {
                            autoAlpha: 0,
                        },
                        {
                            autoAlpha: 1,
                            duration: 0.2,
                        },
                        '-=0.35'
                    );
            }
        },
    },
    setup() {
        const state = computed(() => store.state);

        return {
            state,
        };
    },
};
</script>
